package com.yogapay.boss.domain;

public class FreightRule {

	private int id ;
	
	private double vpay ;
	
	private String create_time ;
	
	private String create_name ;
	
	private double fmoney ;
	
	private double fdistance ;
	
	private double step_distance ;
	
	private double step_distance_money ;
	
	private double fweight ;
	
	private double step_weight ;
	
	private double step_weight_money ;
	
	private String freight_text ;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getVpay() {
		return vpay;
	}

	public void setVpay(double vpay) {
		this.vpay = vpay;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

	public String getCreate_name() {
		return create_name;
	}

	public void setCreate_name(String create_name) {
		this.create_name = create_name;
	}

	public double getFmoney() {
		return fmoney;
	}

	public void setFmoney(double fmoney) {
		this.fmoney = fmoney;
	}

	public double getFdistance() {
		return fdistance;
	}

	public void setFdistance(double fdistance) {
		this.fdistance = fdistance;
	}

	public double getStep_distance() {
		return step_distance;
	}

	public void setStep_distance(double step_distance) {
		this.step_distance = step_distance;
	}

	public double getStep_distance_money() {
		return step_distance_money;
	}

	public void setStep_distance_money(double step_distance_money) {
		this.step_distance_money = step_distance_money;
	}

	public double getFweight() {
		return fweight;
	}

	public void setFweight(double fweight) {
		this.fweight = fweight;
	}

	public double getStep_weight() {
		return step_weight;
	}

	public void setStep_weight(double step_weight) {
		this.step_weight = step_weight;
	}

	public double getStep_weight_money() {
		return step_weight_money;
	}

	public void setStep_weight_money(double step_weight_money) {
		this.step_weight_money = step_weight_money;
	}

	public String getFreight_text() {
		return freight_text;
	}

	public void setFreight_text(String freight_text) {
		this.freight_text = freight_text;
	}
	
	
	
	
}
