package com.yogapay.boss.domain;

public class LgcAddr {
	
	private int id ;
	
	private String lgcNo ;
	
	private String name ;
	
	private String areaId ;
	
	private String areaAddr ;
	
	private String detailAddr ;
	
	private String phone ;
	
	private String addrType ;
	
	private String addrSrc ;
	
	private String courierNo ;
	
	private String incomingPhone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getLgcNo() {
		return lgcNo;
	}

	public void setLgcNo(String lgcNo) {
		this.lgcNo = lgcNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAreaId() {
		return areaId;
	}

	public void setAreaId(String areaId) {
		this.areaId = areaId;
	}

	public String getAreaAddr() {
		return areaAddr;
	}

	public void setAreaAddr(String areaAddr) {
		this.areaAddr = areaAddr;
	}

	public String getDetailAddr() {
		return detailAddr;
	}

	public void setDetailAddr(String detailAddr) {
		this.detailAddr = detailAddr;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getAddrType() {
		return addrType;
	}

	public void setAddrType(String addrType) {
		this.addrType = addrType;
	}

	public String getAddrSrc() {
		return addrSrc;
	}

	public void setAddrSrc(String addrSrc) {
		this.addrSrc = addrSrc;
	}

	public String getCourierNo() {
		return courierNo;
	}

	public void setCourierNo(String courierNo) {
		this.courierNo = courierNo;
	}

	public String getIncomingPhone() {
		return incomingPhone;
	}

	public void setIncomingPhone(String incomingPhone) {
		this.incomingPhone = incomingPhone;
	}

	
}
