package com.yogapay.boss.domain;

public class ProOrder {
	
	private int id ; 
	
	private String orderNo ; 
	
	private String lgcOrderNo ;
	
	private String dealStatus ;
	
	private String proReason ;
	
	private String descb ;
	
	private String lastDesc ;
	
	private String comoperator ;
	
	private String checkName ;
	
	private String createTime ;
	
	private String curSubstationNo ;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getLgcOrderNo() {
		return lgcOrderNo;
	}

	public void setLgcOrderNo(String lgcOrderNo) {
		this.lgcOrderNo = lgcOrderNo;
	}

	public String getDealStatus() {
		return dealStatus;
	}

	public void setDealStatus(String dealStatus) {
		this.dealStatus = dealStatus;
	}

	public String getProReason() {
		return proReason;
	}

	public void setProReason(String proReason) {
		this.proReason = proReason;
	}

	public String getDescb() {
		return descb;
	}

	public void setDescb(String descb) {
		this.descb = descb;
	}

	public String getLastDesc() {
		return lastDesc;
	}

	public void setLastDesc(String lastDesc) {
		this.lastDesc = lastDesc;
	}

	public String getComoperator() {
		return comoperator;
	}

	public void setComoperator(String comoperator) {
		this.comoperator = comoperator;
	}

	public String getCheckName() {
		return checkName;
	}

	public void setCheckName(String checkName) {
		this.checkName = checkName;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getCurSubstationNo() {
		return curSubstationNo;
	}

	public void setCurSubstationNo(String curSubstationNo) {
		this.curSubstationNo = curSubstationNo;
	}

   	
	
}
