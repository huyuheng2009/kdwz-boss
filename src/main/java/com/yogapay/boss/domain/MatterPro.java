package com.yogapay.boss.domain;

public class MatterPro {
	
	private Integer id ;
	
	private String lgcNo ;
	
	private String matterNo ;
	
	private String matterName ;
	
	private int matterType ;

	private String matterUnit ;
	
	private Float matterPrice ;
	
	private Float matterSalePrice ;
	
	private String note ;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getLgcNo() {
		return lgcNo;
	}

	public void setLgcNo(String lgcNo) {
		this.lgcNo = lgcNo;
	}

	public String getMatterNo() {
		return matterNo;
	}

	public void setMatterNo(String matterNo) {
		this.matterNo = matterNo;
	}

	public String getMatterName() {
		return matterName;
	}

	public void setMatterName(String matterName) {
		this.matterName = matterName;
	}

	public int getMatterType() {
		return matterType;
	}

	public void setMatterType(int matterType) {
		this.matterType = matterType;
	}

	public String getMatterUnit() {
		return matterUnit;
	}

	public void setMatterUnit(String matterUnit) {
		this.matterUnit = matterUnit;
	}

	public Float getMatterPrice() {
		return matterPrice;
	}

	public void setMatterPrice(Float matterPrice) {
		this.matterPrice = matterPrice;
	}

	public Float getMatterSalePrice() {
		return matterSalePrice;
	}

	public void setMatterSalePrice(Float matterSalePrice) {
		this.matterSalePrice = matterSalePrice;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	
}
