package com.yogapay.boss.domain;

public class CodInfo {

	private Integer id ;
	
	private String orderNo ;
	
	private String codNo ;
	
	private float codPrice ;
	
	private int payStatus ;
	
	private String payTime ;
	
	private String payType ;
	
	private String payName ;
	
	private String note ;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getCodNo() {
		return codNo;
	}

	public void setCodNo(String codNo) {
		this.codNo = codNo;
	}

	public float getCodPrice() {
		return codPrice;
	}

	public void setCodPrice(float codPrice) {
		this.codPrice = codPrice;
	}

	public int getPayStatus() {
		return payStatus;
	}

	public void setPayStatus(int payStatus) {
		this.payStatus = payStatus;
	}

	public String getPayTime() {
		return payTime;
	}

	public void setPayTime(String payTime) {
		this.payTime = payTime;
	}

	public String getPayType() {
		return payType;
	}

	public void setPayType(String payType) {
		this.payType = payType;
	}

	public String getPayName() {
		return payName;
	}

	public void setPayName(String payName) {
		this.payName = payName;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}
	
	
	
}
